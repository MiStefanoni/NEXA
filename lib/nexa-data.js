const LANGUAGE_NAME_MAP = {
  Arabic: { pt: "Árabe", en: "Arabic" },
  English: { pt: "Inglês", en: "English" },
  French: { pt: "Francês", en: "French" },
  Hindi: { pt: "Hindi", en: "Hindi" },
  Italian: { pt: "Italiano", en: "Italian" },
  Mandarin: { pt: "Mandarim", en: "Mandarin" },
  Portuguese: { pt: "Português", en: "Portuguese" },
  Spanish: { pt: "Espanhol", en: "Spanish" },
  Turkish: { pt: "Turco", en: "Turkish" },
  Urdu: { pt: "Urdu", en: "Urdu" },
};

export function getLangConfig(lang) {
  return UI[lang] || UI.pt;
}

export function getCategoryMeta(slug, lang) {
  return CATEGORY_META[slug]?.[lang] || CATEGORY_META[slug]?.pt;
}

export function getCurrentLang(lang) {
  return lang === "en" ? "en" : "pt";
}

export function normalizeBoolean(value) {
  return String(value).toLowerCase() === "true";
}

export function hasContent(value) {
  return String(value ?? "").trim() !== "";
}

export function getLocationMode(profile) {
  return (profile.remote_or_local || "").trim().toLowerCase() === "remote" ? "remote" : "local";
}

export function getLocalizedField(profile, baseKey, lang, fallback = "") {
  const normalizedLang = getCurrentLang(lang);
  const requested = profile[`${baseKey}_${normalizedLang}`];
  if (hasContent(requested)) {
    return requested;
  }

  if (normalizedLang === "en") {
    const portuguese = profile[`${baseKey}_pt`];
    if (hasContent(portuguese)) {
      return fallback || getLangConfig("en").profileFallback;
    }
  }

  return profile[`${baseKey}_pt`] || fallback;
}

export function getCategoryTitle(profile, lang) {
  return getLocalizedField(
    profile,
    "category",
    lang,
    getCategoryMeta(profile.category_slug, lang)?.title || "",
  );
}

export function getLocalizedLanguages(value, lang) {
  return String(value || "")
    .split(",")
    .map((item) => item.trim())
    .filter(Boolean)
    .map((item) => LANGUAGE_NAME_MAP[item]?.[lang] || item)
    .join(", ");
}

export function localizeDelivery(value, lang) {
  let output = String(value || "");
  if (lang === "pt") {
    output = output.replaceAll("On-site", "Presencial");
    output = output.replaceAll("Remote", "Remoto");
    output = output.replaceAll("Hybrid", "Híbrido");
  }
  return output;
}

export function localizeEngagement(value, lang) {
  let output = String(value || "");
  if (lang === "pt") {
    output = output.replaceAll("Project-based", "Por projeto");
    output = output.replaceAll("Retainer", "Recorrente");
  }
  return output;
}

export function getProfilePath(slug, lang) {
  return `/${getCurrentLang(lang)}/profile/${encodeURIComponent(slug)}`;
}

export function getLegacyProfileUrl(slug, lang) {
  return `/${getCurrentLang(lang)}/profile-template.html?slug=${encodeURIComponent(slug)}`;
}

export function getCategoryPath(slug, lang) {
  return `/${getCurrentLang(lang)}/category/${slug}`;
}

export function getLegacyCategoryUrl(slug, lang) {
  return `/${getCurrentLang(lang)}/category-${slug}.html`;
}

export function getLinkLabel(href, label, fallback) {
  if (hasContent(label)) return label;
  if (hasContent(href)) return String(href).replace(/^https?:\/\//, "");
  return fallback;
}

export function getBadgeLabel(profile, lang) {
  const ui = getLangConfig(lang);
  if (getLocationMode(profile) === "remote") {
    return ui.remote;
  }

  return (profile.location || "").split(",")[0] || ui.local;
}

export function getServiceTitle(profile, index, lang) {
  return getLocalizedField(profile, `service_${index}_title`, lang, "");
}

export function getServiceDescription(profile, index, lang) {
  return getLocalizedField(profile, `service_${index}_description`, lang, getLangConfig(lang).profileFallback);
}

export function getServiceEngagement(profile, index, lang) {
  return localizeEngagement(profile[`service_${index}_engagement`], lang);
}

export function getServiceDelivery(profile, index, lang) {
  return localizeDelivery(profile[`service_${index}_delivery`], lang);
}

export function getPortfolioItem(profile, index, lang) {
  return {
    title: getLocalizedField(profile, `portfolio_${index}_title`, lang, ""),
    description: getLocalizedField(profile, `portfolio_${index}_description`, lang, getLangConfig(lang).profileFallback),
  };
}

export function buildServiceTags(profile, lang) {
  return [1, 2, 3].map((index) => getServiceTitle(profile, index, lang)).filter(Boolean);
}

export function matchesDirectorySearch(profile, query, lang) {
  if (!query) return true;

  const terms = [
    profile.name,
    getCategoryTitle(profile, lang),
    getLocalizedField(profile, "role_title", lang, ""),
    getLocalizedField(profile, "short_bio", lang, ""),
    profile.location,
    getLocalizedLanguages(profile.languages, lang),
    ...buildServiceTags(profile, lang),
  ];

  return terms.join(" ").toLowerCase().includes(query);
}
